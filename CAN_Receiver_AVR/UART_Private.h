#ifndef UART_PRIVATE_H_
#define UART_PRIVATE_H_

/********************************************************************************************
 *
 * 							MACROS FOR BAUD AND CLOCK OF THE CPU
 *
 *******************************************************************************************/
#define BAUD						9600
#define F_CPU						8000000
/********************************************************************************************
 *
 * 							MACRO FOR BAUD RATE
 *
 *******************************************************************************************/
#define BAUD_RATE					((F_CPU)/(BAUD * 16)-1)

/********************************************************************************************
 *
 * 							MACRO FOR UDR I/O data register
 *
 *******************************************************************************************/
#define UDR							*((volatile u8*)(0x2C))


/********************************************************************************************
 *
 * 							MACRO FOR control and status register A  UCSRA
 *
 *******************************************************************************************/
#define UCSRA						*((volatile u8*)(0x2B))

/********************************************************************************************
 *
 * 							MACRO FOR UCSRA pins
 *
 *******************************************************************************************/
#define RXC							7
#define TXC							6
#define UDRE						5
#define FE							4
#define DOR							3
#define PE							2
#define U2X							1
#define MPCM						0

/********************************************************************************************
 *
 * 							MACRO FOR control and status register B  UCSRB
 *
 *******************************************************************************************/
#define UCSRB						*((volatile u8*)(0x2A))

/********************************************************************************************
 *
 * 							MACRO FOR UCSRB pins
 *
 *******************************************************************************************/
#define RXCIE						7
#define TXCIE						6
#define UDRIE						5
#define RXEN						4
#define TXEN						3
#define UCSCZ2						2
#define RXB8						1
#define TXB8						0


/********************************************************************************************
 *
 * 							MACRO FOR control and status register C  UCSRC
 *
 *******************************************************************************************/
#define UCSRC						*((volatile u8*)(0x40))

/********************************************************************************************
 *
 * 							MACRO FOR UCSRC pins
 *
 *******************************************************************************************/
#define URSEL						7
#define UMSEL						6
#define UPM1						5
#define UPM0						4
#define USBS						3
#define UCSZ1						2
#define UCSZ0						1
#define UCPOL						0

/********************************************************************************************
 *
 * 							MACRO FOR BAUD STATE REGISTERS
 *
 *******************************************************************************************/
#define UBRRL						*((volatile u8*)(0x29))
#define UBRRH						*((volatile u8*)(0x40))



#endif /* UART_PRIVATE_H_ */
